/**
 * DOM Backend Tests
 */

import { describe, it, expect } from 'vitest'
import { parse } from '../../compiler/parser/parser'
import { generateDOM } from '../../compiler/backends/dom'

describe('DOM Backend: Basic Generation', () => {
  it('generates valid JavaScript for simple component', () => {
    const ast = parse(`
Card as frame:
  pad 16
  bg #1a1a23

Card
`)
    const js = generateDOM(ast)
    
    expect(js).toContain('// Generated by Mirror Compiler')
    expect(js).toContain('export function createUI')
    expect(js).toContain("document.createElement('div')")
  })

  it('generates styles from properties', () => {
    const ast = parse(`
Button as button:
  pad 12
  bg #3B82F6
  rad 8

Button "Click me"
`)
    const js = generateDOM(ast)
    
    expect(js).toContain("'padding': '12px'")
    expect(js).toContain("'background': '#3B82F6'")
    expect(js).toContain("'border-radius': '8px'")
  })

  it('generates named instance accessors', () => {
    const ast = parse(`
Button as button:
  pad 8

Button named saveBtn "Save"
`)
    const js = generateDOM(ast)
    
    expect(js).toContain("_elements['saveBtn']")
    expect(js).toContain('get saveBtn()')
  })

  it('generates event listeners', () => {
    const ast = parse(`
Button as button:
  onclick toggle()

Button "Toggle"
`)
    const js = generateDOM(ast)

    expect(js).toContain("addEventListener('click'")
  })

  it('generates tokens', () => {
    const ast = parse(`
primary: color = #3B82F6
spacing: size = 16
`)
    const js = generateDOM(ast)

    // Tokens are now stored in __mirrorData and aliased to tokens
    expect(js).toContain('const __mirrorData = window.__mirrorData = {')
    expect(js).toContain('"primary": "#3B82F6"')
    expect(js).toContain('"spacing":')
  })

  it('generates hover state as CSS', () => {
    const ast = parse(`
Card as frame:
  pad 16
  bg #333
  hover:
    bg #555

Card
`)
    const js = generateDOM(ast)

    // Hover is now CSS, not _stateStyles
    expect(js).toContain(':hover {')
    expect(js).toContain('background: #555')
  })

  it('generates children', () => {
    const ast = parse(`
Card as frame:
  pad 16

Text as text:

Card
  Text "Hello"
`)
    const js = generateDOM(ast)
    
    expect(js).toContain('appendChild')
  })
})

