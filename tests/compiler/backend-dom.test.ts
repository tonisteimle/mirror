describe('DOM Backend: Each Loop', () => {
  it('generates each loop container', () => {
    const ast = parse(`
Card as frame:
  pad 16

each $task in $tasks
  Card $task.title
`)
    const js = generateDOM(ast)

    expect(js).toContain('// Each loop: task in tasks')
    expect(js).toContain('data-each-container')
    expect(js).toContain('_eachConfig')
    expect(js).toContain("itemVar: 'task'")
    expect(js).toContain("collection: 'tasks'")
  })

  it('generates renderItem function', () => {
    const ast = parse(`
Card as frame:

each $task in $tasks
  Card $task.title
`)
    const js = generateDOM(ast)

    expect(js).toContain('renderItem: (task, index) => {')
    expect(js).toContain('dataset.eachItem')
  })

  it('generates initial render with data', () => {
    const ast = parse(`
Item as frame:

each $item in $items
  Item $item.name
`)
    const js = generateDOM(ast)

    // Each loop now uses $get for data access
    expect(js).toContain('$get')
    expect(js).toContain('items')
  })

  it('handles filter in each loop', () => {
    const ast = parse(`
Task as frame:

each $task in $tasks where $task.done == false
  Task $task.title
`)
    const js = generateDOM(ast)

    expect(js).toContain('filterFn:')  // Function instead of string to avoid eval()
    expect(js).toContain('tasksFiltered')
  })

  it('includes update method for reactive loops', () => {
    const ast = parse(`Card as frame:`)
    const js = generateDOM(ast)

    expect(js).toContain('update()')
    expect(js).toContain("querySelectorAll('[data-each-container]')")
    expect(js).toContain('renderItem')
  })
})

describe('DOM Backend: Conditionals', () => {
  it('generates conditional container', () => {
    const ast = parse(`
Button as button:

if (loggedIn)
  Button "Logout"
`)
    const js = generateDOM(ast)

    expect(js).toContain('// Conditional')
    expect(js).toContain('data-conditional-id')
    expect(js).toContain('_conditionalConfig')
  })

  it('generates condition function', () => {
    const ast = parse(`
Avatar as frame:

if (isAdmin)
  Avatar
`)
    const js = generateDOM(ast)

    // Variables in conditions are wrapped with $get() for data context lookup
    expect(js).toContain('condition: () => ($get("isAdmin"))')
  })

  it('generates renderThen function', () => {
    const ast = parse(`
Panel as frame:

if (showPanel)
  Panel
`)
    const js = generateDOM(ast)

    expect(js).toContain('renderThen: () => {')
    expect(js).toContain('createDocumentFragment')
  })

  it('generates renderElse function for if-else', () => {
    const ast = parse(`
Avatar as frame:
LoginButton as button:

if (loggedIn)
  Avatar
else
  LoginButton "Login"
`)
    const js = generateDOM(ast)

    expect(js).toContain('renderThen: () => {')
    expect(js).toContain('renderElse: () => {')
  })

  it('generates initial conditional render', () => {
    const ast = parse(`
Content as frame:

if (hasData)
  Content
`)
    const js = generateDOM(ast)

    expect(js).toContain('// Initial conditional render')
    // Variables in conditions are wrapped with $get() for data context lookup
    expect(js).toContain('if (($get("hasData"))) {')
    expect(js).toContain('renderThen()')
  })

  it('includes conditionals in update method', () => {
    const ast = parse(`Card as frame:`)
    const js = generateDOM(ast)

    expect(js).toContain("querySelectorAll('[data-conditional-id]')")
    expect(js).toContain('_conditionalConfig')
    expect(js).toContain('condition()')
  })

  it('handles complex condition expressions', () => {
    const ast = parse(`
Panel as frame:

if (user.isAdmin && hasPermission)
  Panel
`)
    const js = generateDOM(ast)

    // Variables in conditions are wrapped with $get() for data context lookup
    expect(js).toContain('condition: () => ($get("user.isAdmin") && $get("hasPermission"))')
  })
})

describe('DOM Backend: Slots', () => {
  it('renders component with slot definitions', () => {
    const ast = parse(`
Card as frame:
  pad 16
  Title:
  Content:

Card
  Title "Welcome"
  Content "Get started"
`)
    const js = generateDOM(ast)

    // Should render the Title and Content as children
    expect(js).toContain("'padding': '16px'")
    expect(js).toContain('appendChild')
  })

  it('slot fillers replace slot defaults', () => {
    const ast = parse(`
Header as frame:
  Label:

Label as text:
  col #999

Header
  Label "Custom Title"
`)
    const js = generateDOM(ast)

    // The Label slot should be filled with "Custom Title"
    expect(js).toContain('Custom Title')
  })

  it('unfilled slots use default content', () => {
    const ast = parse(`
Panel as frame:
  Title "Default"

Panel
`)
    const js = generateDOM(ast)

    // Should still render the default "Default" content
    expect(js).toContain('Default')
  })

  it('handles multiple children filling same-name slots', () => {
    const ast = parse(`
List as frame:
  Item:

Item as frame:
  pad 8

List
  Item "First"
  Item "Second"
`)
    const js = generateDOM(ast)

    expect(js).toContain('First')
    expect(js).toContain('Second')
  })
})

