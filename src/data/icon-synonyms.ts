/**
 * Get all synonyms for an icon name
 */
export function getIconSynonyms(iconName: string): string[] {
  return iconSynonyms[iconName] || []
}

/**
 * Check if a query matches an icon (by name or synonyms)
 */
export function iconMatchesQuery(iconName: string, query: string): boolean {
  const lowerQuery = query.toLowerCase()
  const lowerIconName = iconName.toLowerCase()

  // Direct name match (PascalCase or kebab-case)
  if (lowerIconName.includes(lowerQuery)) {
    return true
  }

  // kebab-case match
  const kebabName = iconName.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
  if (kebabName.includes(lowerQuery)) {
    return true
  }

  // Synonym match
  const synonyms = iconSynonyms[iconName]
  if (synonyms) {
    return synonyms.some(synonym => synonym.toLowerCase().includes(lowerQuery))
  }

  return false
}

/**
 * Search icons with synonym support, returns sorted by relevance
 */
export function searchIcons(allIconNames: string[], query: string): string[] {
  if (!query) return allIconNames

  const lowerQuery = query.toLowerCase()

  // Score each icon for relevance
  const scored = allIconNames
    .map(iconName => {
      const lowerIconName = iconName.toLowerCase()
      const kebabName = iconName.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
      const synonyms = iconSynonyms[iconName] || []

      let score = 0

      // Exact match = highest score
      if (lowerIconName === lowerQuery || kebabName === lowerQuery) {
        score = 100
      }
      // Starts with query = high score
      else if (lowerIconName.startsWith(lowerQuery) || kebabName.startsWith(lowerQuery)) {
        score = 80
      }
      // Contains query in name = medium-high score
      else if (lowerIconName.includes(lowerQuery) || kebabName.includes(lowerQuery)) {
        score = 60
      }
      // Exact synonym match = medium score
      else if (synonyms.some(s => s.toLowerCase() === lowerQuery)) {
        score = 50
      }
      // Synonym starts with query = lower-medium score
      else if (synonyms.some(s => s.toLowerCase().startsWith(lowerQuery))) {
        score = 40
      }
      // Synonym contains query = lower score
      else if (synonyms.some(s => s.toLowerCase().includes(lowerQuery))) {
        score = 30
      }

      return { iconName, score }
    })
    .filter(item => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .map(item => item.iconName)

  return scored
}
